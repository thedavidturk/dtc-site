/**
 * Writes a layout.tsx per Perspectives article exporting its metadata:
 * page title + the animated GIF share card in /public/og/. The article
 * pages are client components, so metadata must live in a layout.
 *
 * Usage: node scripts/write-og-layouts.mjs
 */
import { writeFileSync, existsSync, readFileSync } from "node:fs";
import { join } from "node:path";
import { ARTICLES } from "./og-articles.mjs";

const ROOT = process.cwd();

for (const [slug, art] of Object.entries(ARTICLES)) {
  // NEVER clobber a hand-maintained layout (several articles carry full SEO
  // metadata: descriptions, keywords, canonical). Only regenerate files this
  // script created (identified by its own marker comment).
  const target = join(ROOT, "src/app/insights", slug, "layout.tsx");
  if (
    existsSync(target) &&
    !readFileSync(target, "utf8").includes("Generated by scripts/write-og-layouts")
  ) {
    console.log(`skip (hand-maintained): src/app/insights/${slug}/layout.tsx`);
    continue;
  }
  // Crawlers only render a static frame (and LinkedIn rejects >5MB),
  // so og:image points at the JPG still; the animated GIFs stay for manual posting.
  const still = `/og/${slug}.jpg`;
  const layout = `import type { Metadata } from "next";

// Generated by scripts/write-og-layouts.mjs — edit scripts/og-articles.mjs
// and re-run instead of hand-editing.
export const metadata: Metadata = {
  title: ${JSON.stringify(art.title)},
  openGraph: {
    title: ${JSON.stringify(art.title)},
    type: "article",
    images: [
      {
        url: ${JSON.stringify(still)},
        width: 1200,
        height: 630,
        type: "image/jpeg",
        alt: ${JSON.stringify(art.title)},
      },
    ],
  },
  twitter: {
    card: "summary_large_image",
    images: [${JSON.stringify(still)}],
  },
};

export default function ArticleLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return children;
}
`;
  writeFileSync(join(ROOT, "src/app/insights", slug, "layout.tsx"), layout);
  console.log(`wrote src/app/insights/${slug}/layout.tsx`);
}
